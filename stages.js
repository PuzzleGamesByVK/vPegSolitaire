/**
 * Normalizes old style IDs (e.g. 525) to 9x9 indices (0-80)
 * Assumes the playable cross is centered in the 9x9 grid.
 */
export const convertTo9x9 = (pegIds) => {
    const board = new Int8Array(81).fill(-1); // -1 = Outside
    // Define the valid Lazlo Cross (English style cross)
    for (let i = 0; i < 81; i++) {
        const r = Math.floor(i / 9);
        const c = i % 9;
        if ((r >= 3 && r <= 5) || (c >= 3 && c <= 5)) {
            if (r >= 1 && r <= 7 && c >= 1 && c <= 7) board[i] = 0; // 0 = Empty Base
        }
    }
    pegIds.forEach(id => {
        //const row = Math.floor(id / 100);
        //const col = Math.floor((id % 100) / 10);
        // Map row/col to 0-8 range for the 9x9 grid
        //const targetIdx = ABCcds.indexOf(pegIds);
        //if (targetIdx >= 0 && targetIdx < 81) board[targetIdx] = 1; // 1 = Peg
       board[ABCcds.indexOf(pegIds)] = 1; // 1 = Peg
    });
    return board;
}; 

